# Basic operations
print(9 + 10)

print(9 - 6)

print(6 * 9)

print (420 / 10)

# More advanced stuff
# ** means power/indice, this is effectively 5^3
print(5 ** 3)

# // means integer division/floor, which gives a 0dp truncated result/always gives an integer.
print(5 // 3)

# % means modulus, which gives the remainder of an division.
print(5 % 3)
